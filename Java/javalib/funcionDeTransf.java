    private boolean checkOrdenFiltro(){
    String orden = textOrdenFiltro.getText();
    int ordenNumero = Integer.parseInt(orden);
        
        if((!orden.isEmpty())){
            if(ordenNumero == 0){
                JOptionPane.showMessageDialog(null, "Caracter Invalido");
                return false;
            }else{
                if(ordenNumero > 20){
                    JOptionPane.showMessageDialog(null, "Filtros Menores a Orden 20");
                    return false;
                }else{
                    if(ordenNumero%2 != 0){
                        JOptionPane.showMessageDialog(null, "Solo se aceptan filtros de orden par");
                        return false;}
                    else
                        return true;
                }
            }
        }
        return true;    
    }
    
    
