    private void cargarDatos(){
        hMapAjustes.put(resolucionDirectividad.getName(), Integer.valueOf(resolucionDirectividad.getSelectedItem().toString()));
        if(planoHorizontal.isSelected() == true)
            hMapAjustes.put("planoDirectividad", "planoHorizontal");
        else
            hMapAjustes.put("planoDirectividad", "planoVertical");
        hMapAjustes.put(resolucionFiltro.getName(), resolucionFiltro.getSelectedItem().toString());
        hMapAjustes.put(tipoFiltro.getName(), tipoFiltro.getSelectedItem().toString());
        if(ordenMinimo.isSelected() == true)
            hMapAjustes.put("ordenFiltro", Integer.valueOf("2"));
        else
            hMapAjustes.put("ordenFiltro", Integer.valueOf(textOrdenFiltro.getText()));
        
        if(guardarResultados.isSelected() == true)
            hMapAjustes.put("guardarResultados", Integer.valueOf("1"));
        else
            hMapAjustes.put("guardarResultados", Integer.valueOf("0"));
        if(giroCompleto.isSelected() == true){
            hMapAjustes.put("medicionInicial", Integer.valueOf("0"));
            hMapAjustes.put("medicionFinal", Integer.valueOf("360"));
        }
        else{
            hMapAjustes.put("medicionInicial", Integer.valueOf(textAperturaInicial.getText()));
            hMapAjustes.put("medicionFinal", Integer.valueOf(textAperturaFinal.getText()));
        }
        hMapAjustes.put(directorioResultados.getName(), directorioResultados.getText());
        WriterObject(hMapAjustes);
    }
    
    public void WriterObject(Object obj){
        try{
            String strFilename = directory + "\\" + filename;
               FileOutputStream fileOut = new FileOutputStream(strFilename);
	       ObjectOutputStream out = new ObjectOutputStream(fileOut);
	       out.writeObject(obj);
	       out.close();
        }catch(IOException ex){
            ex.printStackTrace();
        }
    }
    
    public Object ReadObject() {
    Object obj = null;
    String strFilename = directory + "\\" + filename;

        try {
            File file = new File(strFilename);
         // Read object only if file exist
            if (file.exists()) {
                ObjectInputStream in = new ObjectInputStream(new
                FileInputStream(strFilename));
		obj = in.readObject();
		in.close();
            }
	}catch (IOException ex) {
		ex.printStackTrace();
	}catch (ClassNotFoundException cexp) {
		cexp.printStackTrace();
	}

    return obj;
    }
